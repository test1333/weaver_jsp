package feilida.finance;

import org.json.JSONException;

import weaver.general.BaseBean;
/**
 * 获取FI05-供应商一般付款申请表单（非银企直联）-供应商未清项明细表
 * @author jianyong
 *
 */
public class GetFinanceFI05 {
	BaseBean log = new BaseBean();
	public String getResult(String workflowId, String action,String code,String custom,String BZ){
		java.util.Map<String, String> oaDatas = new java.util.HashMap<String, String>();
		oaDatas.put("I_ACTION", action);
		oaDatas.put("I_BUKRS", custom);
		oaDatas.put("I_LIFNR", code);
		oaDatas.put("I_ZZB", BZ);
		tmc.BringMainAndDetailByMain bmb = new tmc.BringMainAndDetailByMain("81");
		String result = bmb.getReturn(oaDatas,workflowId); 
		return result;
}
	
	
	public String getDetialinfo(String code,String custom,String BZ){
		log.writeLog("result:code"+code+" custom:"+custom+" BZ:"+BZ);
		String result=getResult("12","I",code,custom,BZ);
		log.writeLog("result"+result);
		StringBuffer sb = new StringBuffer();
		try {
			org.json.JSONObject json = new org.json.JSONObject(result);
			org.json.JSONArray jsonArr = json.getJSONObject("table").getJSONArray("main");	
			for(int index=0;index<jsonArr.length();index++){
				org.json.JSONObject jsonx = (org.json.JSONObject)(jsonArr.get(index));
				String LIFNR = jsonx.getString("LIFNR").replace("'","'||chr(39)||'").replace("&","'||chr(38)||'");//供应商编号
				String BELNR = jsonx.getString("BELNR").replace("'","'||chr(39)||'").replace("&","'||chr(38)||'");//凭证编号
				String BUDAT = jsonx.getString("BUDAT").replace("'","'||chr(39)||'").replace("&","'||chr(38)||'");//凭证过账日期
				String WAERS = jsonx.getString("WAERS").replace("'","'||chr(39)||'").replace("&","'||chr(38)||'");//币种
				String SGTXT = jsonx.getString("SGTXT").replace("'","'||chr(39)||'").replace("&","'||chr(38)||'");//文本
				String BLDAT = jsonx.getString("BLDAT").replace("'","'||chr(39)||'").replace("&","'||chr(38)||'");//发票日期
				String GJAHR = jsonx.getString("GJAHR").replace("'","'||chr(39)||'").replace("&","'||chr(38)||'");//财年
				
				String ZUONR = jsonx.getString("ZUONR").replace("'","'||chr(39)||'").replace("&","'||chr(38)||'");//发票号码
				String BUZEI  = jsonx.getString("BUZEI").replace("'","'||chr(39)||'").replace("&","'||chr(38)||'");//凭证行项目数
				
				String DMBTR = jsonx.getString("DMBTR").replace("'","'||chr(39)||'").replace("&","'||chr(38)||'");//本位币金额
				String WRBTR = jsonx.getString("WRBTR").replace("'","'||chr(39)||'").replace("&","'||chr(38)||'");//原币金额
				
				sb.append(LIFNR);//供应商编号
				sb.append("###");
				sb.append(BELNR);	//凭证编号
				sb.append("###");
				sb.append(BUDAT);//凭证过账日期
				sb.append("###");
				sb.append(WRBTR);//原币金额
				sb.append("###");
				sb.append(DMBTR);//本位币金额
				sb.append("###");
				sb.append(WAERS);//币种
				sb.append("###");
				sb.append(BLDAT);//发票日期
				sb.append("###");
				sb.append(ZUONR);//发票号码
				sb.append("###");
				sb.append(GJAHR);//财年
				sb.append("###");
				sb.append(BUZEI);//凭证行项目数
				sb.append("###");
				sb.append(SGTXT);//文本
	
				sb.append("@@@");
				
				
			}
		} catch (JSONException e) {
			e.printStackTrace();
		}
		return sb.toString();	
		
	}

}
